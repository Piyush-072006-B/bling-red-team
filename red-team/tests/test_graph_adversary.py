class TestBipartiteGate:
    def test_bipartite_density_below_07(self):
        """Effective bipartite density must be below the 0.7 trigger threshold."""
        result = generate_bypass("bipartite")
        sg = result["synthetic_subgraph"]
        density = sg["effective_bipartite_density"]
        threshold = sg["trigger_threshold"]

        assert density < threshold, (
            f"Bipartite density {density} is not below trigger threshold {threshold}"
        )

    def test_bipartite_split_is_4_and_3(self):
        """Senders must be split 4 + 3."""
        result = generate_bypass("bipartite")
        sg = result["synthetic_subgraph"]
        assert sg["batch_a_senders"] == 4
        assert sg["batch_b_senders"] == 3
        assert sg["batch_a_senders"] + sg["batch_b_senders"] == 7


